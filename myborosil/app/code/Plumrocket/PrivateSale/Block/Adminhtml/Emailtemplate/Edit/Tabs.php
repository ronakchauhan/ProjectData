namespace Plumrocket\PrivateSale\Block\Adminhtml\Emailtemplate\Edit;

/**
 * Admin page left menu
 */
class Tabs extends \Magento\Backend\Block\Widget\Tabs
{
    /**
     * @return void
     */
    protected function _construct()
    {
        parent::_construct();
        $this->setId('privatesale_emailtemplate_tabs');
        $this->setDestElementId('edit_form');

        if ($this->_request->getParam('id')) {
            $this->setTitle(__('Edit Newsletter Template'));
        } else {
            $this->setTitle(__('Add New Newsletter Template'));
        }
    }
}
